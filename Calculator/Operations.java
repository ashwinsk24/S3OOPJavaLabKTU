package Calculator;
import java.io.*;
import java.util.*;

public class Operations
{
  public int sum(int a,int b)
  {
    int s=a+b;
    return s;
  }

  public int difference(int a,int b)
  {
    int s=a-b;
    return s;
  }
  
  public int product(int a,int b)
  {
    int s=a*b;
    return s;
  }

  public int quotient(int a,int b)
  {
    int s=a/b;
    return s;
  }
}